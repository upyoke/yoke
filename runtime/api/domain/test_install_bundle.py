def test_bundle_ships_rendered_agent_adapters(conn) -> None:
    # A managed project needs the lifecycle subagents (Architect/Engineer/
    # Tester/…) to run /yoke conduct etc., so the bundle ships the rendered
    # adapters for both harnesses plus the Claude references tree. (The earlier
    # "product-safe" boundary that withheld these — and the matching
    # no-internal-recipes guard — was removed: it left managed projects unable
    # to run any lifecycle command. The full operating layer is the contract;
    # scrubbing operator-debug fallback recipes from skills is separate work.)
    bundle = install_bundle.build_bundle(1, conn)

    paths = {e["path"] for e in bundle["files"]}
    roles = (
        "architect",
        "boss",
        "engineer",
        "product-designer",
        "product-manager",
        "simulator",
        "tester",
    )
    for role in roles:
        assert f".claude/agents/yoke-{role}.md" in paths
        assert f".codex/agents/yoke-{role}.toml" in paths
    assert any(
        p.startswith(".claude/agents/references/") for p in paths
    ), "the Claude agent references tree must ship"
    # Session rules the lifecycle skills assume are installed.
    assert ".claude/rules/session.md" in paths


