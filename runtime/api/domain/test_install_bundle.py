def test_build_bundle_round_trips_through_apply_bundle(conn, tmp_path) -> None:
    # The server renders the bundle; the client installer applies it. These
    # halves are otherwise tested with hand-built dicts and never meet — this
    # crosses the producer->consumer seam with the REAL rendered bundle so a
    # shape mismatch (e.g. a path apply_bundle's safety guard would reject)
    # cannot hide.
    from yoke_core.domain.project_install import apply_bundle

    bundle = install_bundle.build_bundle(2, conn)
    repo = tmp_path / "proj"
    repo.mkdir()

    report = apply_bundle(repo, bundle, source="test")

    bundle_paths = {e["path"] for e in bundle["files"]}
    # Every rendered file landed on disk and is recorded in the manifest.
    assert set(report["files_written"]) == bundle_paths
    for path in bundle_paths:
        assert (repo / path).is_file()
    manifest = json.loads(
        (repo / ".yoke/install-manifest.json").read_text("utf-8")
    )
    assert set(manifest["files"]) == bundle_paths
    # Hook subtrees merged into the harness settings files.
    assert (repo / ".claude/settings.json").is_file()
    assert (repo / ".codex/hooks.json").is_file()
    # Seed-if-missing project contract landed.
    for entry in bundle["project_contract_files"]:
        assert (repo / entry["path"]).is_file()


