def test_bundle_carries_project_contract_files(conn) -> None:
    from yoke_core.domain import project_contract

    bundle = install_bundle.build_bundle(2, conn)

    contract = bundle["project_contract_files"]
    assert contract == project_contract.bundle_contract_files("ExternalWebapp"), (
        "contract entries render from the project's display name"
    )
    paths = [e["path"] for e in contract]
    assert paths == sorted(paths)
    assert len(paths) == len(set(paths))
    for e in contract:
        assert e["path"].startswith(".yoke/")
        assert e["install_policy"] == project_contract.SEED_IF_MISSING
        assert e["category"] == project_contract.CATEGORY_PROJECT_POLICY
    managed = {e["path"] for e in bundle["files"]}
    assert managed.isdisjoint(paths), (
        "contract files and managed files are disjoint namespaces"
    )


