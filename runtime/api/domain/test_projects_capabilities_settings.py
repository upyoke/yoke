def _apply_capability_schema() -> None:
    """Create ``projects`` + ``project_capabilities`` (canonical columns)."""
    conn = db_backend.connect()
    try:
        conn.execute(
            """
            CREATE TABLE projects (
                id BIGINT PRIMARY KEY,
                slug TEXT NOT NULL UNIQUE,
                name TEXT NOT NULL,
                public_item_prefix TEXT NOT NULL DEFAULT 'YOK'
            )
            """
        )
        conn.execute(
            """
            INSERT INTO projects (id, slug, name, public_item_prefix)
            VALUES (1, 'yoke', 'Yoke', 'YOK'), (2, 'externalwebapp', 'ExternalWebapp', 'EXT')
            """
        )
        conn.execute(
            """
            CREATE TABLE project_capabilities (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                project_id BIGINT NOT NULL REFERENCES projects(id),
                type TEXT NOT NULL,
                settings TEXT DEFAULT '{}',
                verified_at TEXT,
                created_at TEXT NOT NULL,
                UNIQUE(project_id, type)
            )
            """
        )
        conn.commit()
    finally:
        conn.close()


@pytest.fixture
def cap_db(tmp_path: Path) -> Iterator[str]:
    """Per-test Postgres DB with the capability tables applied."""
    with init_test_db(tmp_path, apply_schema=_apply_capability_schema) as db_path:
        yield db_path


def _settings(db_path: str, cap_type: str = "docker") -> dict:
    raw = pcs.cmd_capability_get_settings("yoke", cap_type, db_path=db_path)
    assert raw is not None
    return json.loads(raw)


