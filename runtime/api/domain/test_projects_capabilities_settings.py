class TestMergeSettings:
    def test_merge_creates_absent_capability(self, cap_db: str) -> None:
        msg = pcs.cmd_capability_merge_settings(
            "yoke", "docker", {"host": "a"}, db_path=cap_db
        )
        assert "docker" in msg
        assert _settings(cap_db) == {"host": "a"}

    def test_sequential_key_merges_compose(self, cap_db: str) -> None:
        pcs.cmd_capability_merge_settings(
            "yoke", "docker", {"host": "a"}, db_path=cap_db
        )
        pcs.cmd_capability_merge_settings(
            "yoke", "docker", {"runtime.network": "bridge"}, db_path=cap_db
        )
        final = _settings(cap_db)
        assert final["host"] == "a"
        assert final["runtime"]["network"] == "bridge"

    def test_merge_retries_once_when_base_moves(
        self, cap_db: str, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        pcs.cmd_capability_set_settings(
            "yoke", "docker", '{"host":"a"}', create=True, db_path=cap_db
        )
        real_read = pcs._read_settings_text
        calls = {"n": 0}

        def contended_read(conn, project_id, cap_type):
            calls["n"] += 1
            if calls["n"] == 1:
                return '{"host":"stale"}'
            return real_read(conn, project_id, cap_type)

        monkeypatch.setattr(pcs, "_read_settings_text", contended_read)
        pcs.cmd_capability_merge_settings(
            "yoke", "docker", {"registry": "ecr"}, db_path=cap_db
        )
        final = _settings(cap_db)
        assert final == {"host": "a", "registry": "ecr"}
        assert calls["n"] >= 2

    def test_merge_conflict_after_retry_is_typed(
        self, cap_db: str, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        pcs.cmd_capability_set_settings(
            "yoke", "docker", '{"host":"a"}', create=True, db_path=cap_db
        )
        monkeypatch.setattr(
            pcs,
            "_read_settings_text",
            lambda conn, project_id, cap_type: '{"host":"stale"}',
        )
        with pytest.raises(SettingsConflictError, match="settings_conflict"):
            pcs.cmd_capability_merge_settings(
                "yoke", "docker", {"registry": "ecr"}, db_path=cap_db
            )
