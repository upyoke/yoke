class TestSelectBodyForGithub:
    def test_under_budget_returns_full(self, evidence_conn: Any):
        full_body = "small body"
        chosen, mode = bb.select_body_for_github(
            full_body,
            item_fields={"title": "t", "status": "idea"},
            conn=evidence_conn,
            item_id=1,
        )
        assert chosen == full_body
        assert mode == "full"

    def test_over_budget_returns_compact(
        self, evidence_conn: Any,
    ):
        big_body = "a" * (bb.GITHUB_BODY_BUDGET_BYTES + 100)
        chosen, mode = bb.select_body_for_github(
            big_body,
            item_fields={
                "title": "t",
                "project": "yoke",
                "status": "implementing",
                "type": "issue",
            },
            conn=evidence_conn,
            item_id=42,
        )
        assert mode == "compact"
        assert bb.body_exceeds_budget(chosen) is False
        assert "YOK-42" in chosen


class TestSelectAndWriteBodyFile:
    def test_writes_full_body_to_temp_file_when_under_budget(
        self, evidence_conn: Any, tmp_path,
    ):
        full_body = "tiny body content"
        path, mode = bb.select_and_write_body_file(
            full_body,
            item_fields={"title": "t", "status": "idea"},
            conn=evidence_conn,
            item_id=1,
            prefix="yoke-test-",
        )
        try:
            assert mode == "full"
            with open(path, "r", encoding="utf-8") as f:
                assert f.read() == full_body
        finally:
            bb.unlink_quiet(path)

    def test_writes_compact_mirror_when_over_budget(
        self, evidence_conn: Any,
    ):
        big_body = "a" * (bb.GITHUB_BODY_BUDGET_BYTES + 100)
        path, mode = bb.select_and_write_body_file(
            big_body,
            item_fields={"title": "t", "status": "idea"},
            conn=evidence_conn,
            item_id=42,
            prefix="yoke-test-",
        )
        try:
            assert mode == "compact"
            with open(path, "r", encoding="utf-8") as f:
                content = f.read()
            assert "YOK-42" in content
            assert "DB" in content
            assert bb.body_exceeds_budget(content) is False
        finally:
            bb.unlink_quiet(path)


class TestEmitCompactNotice:
    def test_emits_only_on_compact_mode(self, capsys):
        import sys
        bb.emit_compact_notice("full", 42, sys.stderr)
        captured = capsys.readouterr()
        assert captured.err == ""

        bb.emit_compact_notice("compact", 42, sys.stderr)
        captured = capsys.readouterr()
        assert "YOK-42" in captured.err
        assert "compact mirror" in captured.err
