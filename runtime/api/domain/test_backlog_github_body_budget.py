_TRANSITIONS_DDL = """
CREATE TABLE item_status_transitions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    item_id INTEGER NOT NULL,
    task_num INTEGER,
    from_status TEXT,
    to_status TEXT NOT NULL,
    source TEXT,
    session_id TEXT,
    actor_id INTEGER,
    project_id INTEGER,
    created_at TEXT NOT NULL
)
"""


def _disposable_conn(ddl: Optional[str] = None) -> Any:
    """Disposable Postgres DB connection, optionally seeded with *ddl*.

    Autocommit: the renderer's best-effort reads (item ref, evidence)
    swallow failures on this minimal schema; autocommit keeps one failed
    probe from poisoning the statements that follow it.
    """
    name = pg_testdb.create_test_database()
    conn = pg_testdb.drop_database_on_close(
        pg_testdb.connect_test_database(name), name,
    )
    conn.autocommit = True
    if ddl:
        conn.execute(ddl)
    return conn


@pytest.fixture
def evidence_conn() -> Iterator[Any]:
    """Disposable Postgres DB seeded with a minimal transition row."""
    conn = _disposable_conn(_TRANSITIONS_DDL)
    conn.execute(
        "INSERT INTO item_status_transitions "
        "(item_id, from_status, to_status, created_at) VALUES (%s, %s, %s, %s)",
        (42, "refined-idea", "implementing", "2026-05-13T20:00:00Z"),
    )
    yield conn
    conn.close()


class TestRenderCompactMirror:
    def test_contains_required_fields(self, evidence_conn: Any):
        """AC-3: compact mirror names title, YOK-N, project, status, lifecycle,
        DB pointer, lifecycle commands, and evidence summary."""
        fields = {
            "title": "My Title",
            "project": "yoke",
            "status": "implementing",
            "type": "issue",
        }
        out = bb.render_compact_mirror(fields, conn=evidence_conn, item_id=42)
        assert "[YOK-42] My Title" in out
        assert "YOK-42" in out
        assert "yoke" in out
        assert "implementing" in out
        assert (
            "python3 -m yoke_core.cli.db_router items get YOK-42 body"
            in out
        )
        # Lifecycle commands include the implementing-phase entry.
        assert "/yoke advance YOK-42 reviewing-implementation" in out
        # Evidence summary present.
        assert "latest transition: refined-idea -> implementing" in out

    def test_unknown_status_falls_back_to_do(
        self, evidence_conn: Any,
    ):
        fields = {"title": "x", "status": "unknown-status", "type": "issue"}
        out = bb.render_compact_mirror(fields, conn=evidence_conn, item_id=1)
        assert "/yoke do YOK-1" in out

    def test_missing_evidence_falls_back_to_no_recent_evidence(self):
        empty = _disposable_conn(_TRANSITIONS_DDL)
        out = bb.render_compact_mirror(
            {"title": "x", "status": "idea"}, conn=empty, item_id=1,
        )
        assert "no recent evidence" in out

    def test_evidence_read_failure_falls_back_quietly(self):
        """Evidence read failures must not crash the whole sync."""
        broken = _disposable_conn()
        # No item_status_transitions table → execute() raises.
        out = bb.render_compact_mirror(
            {"title": "x", "status": "idea"}, conn=broken, item_id=1,
        )
        assert "no recent evidence" in out

    def test_long_title_truncated(self, evidence_conn: Any):
        """AC `Watch Out For`: cap title length to keep mirror under budget."""
        long_title = "x" * 5000
        out = bb.render_compact_mirror(
            {"title": long_title, "status": "idea"},
            conn=evidence_conn,
            item_id=1,
        )
        # Truncated title ends with the ellipsis sentinel.
        assert "…" in out
        # The mirror line for the title is shorter than the raw title.
        assert long_title not in out

    def test_compact_mirror_fits_under_budget(
        self, evidence_conn: Any,
    ):
        """The compact mirror itself MUST fit under the budget."""
        long_title = "x" * 5000  # title truncates to ~500 chars
        out = bb.render_compact_mirror(
            {
                "title": long_title,
                "project": "yoke",
                "status": "implementing",
                "type": "issue",
            },
            conn=evidence_conn,
            item_id=42,
        )
        assert bb.body_exceeds_budget(out) is False


