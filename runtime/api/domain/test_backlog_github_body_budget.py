# Pure predicate
# ---------------------------------------------------------------------------


class TestBodyExceedsBudget:
    def test_under_budget_returns_false(self):
        assert bb.body_exceeds_budget("a" * 1000) is False

    def test_exactly_at_budget_returns_false(self):
        body = "a" * bb.GITHUB_BODY_BUDGET_BYTES
        assert bb.body_exceeds_budget(body) is False

    def test_one_byte_over_budget_returns_true(self):
        body = "a" * (bb.GITHUB_BODY_BUDGET_BYTES + 1)
        assert bb.body_exceeds_budget(body) is True

    def test_byte_length_vs_char_length(self):
        """AC-2: predicate is byte-length, not char-length.

        A 31k-char emoji body is ~124k bytes because each emoji code point
        encodes as four UTF-8 bytes.  Char-length-based logic would falsely
        accept it; the byte-length predicate must reject.
        """
        # Each "🚀" is 4 bytes in UTF-8.  31 000 chars → 124 000 bytes.
        body = "🚀" * 31000
        assert len(body) == 31000  # char-length passes
        assert len(body.encode("utf-8")) == 124000  # byte-length exceeds
        assert bb.body_exceeds_budget(body) is True


