@pytest.mark.parametrize(
    "reader",
    (
        bootstrap_project_helpers._capability_secret,
        validate_webapp_pipeline_helpers._capability_secret,
    ),
)
def test_private_capability_readers_refuse_retired_github_secrets(reader) -> None:
    with pytest.raises(ValueError, match="retired and cannot be read"):
        reader(object(), "yoke", " GitHub ", "token")


