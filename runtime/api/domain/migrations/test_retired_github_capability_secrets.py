def test_apply_removes_only_normalized_github_rows(conn: Any) -> None:
    unrelated_before = [
        dict(row) for row in conn.execute(
            "SELECT project_id, type, key, value, source "
            "FROM capability_secrets "
            "WHERE LOWER(TRIM(type)) <> 'github' ORDER BY id"
        ).fetchall()
    ]

    migration.apply(conn)
    conn.commit()
    migration.invariants(conn)

    assert conn.execute(
        "SELECT COUNT(*) FROM capability_secrets "
        "WHERE LOWER(TRIM(type)) = 'github'"
    ).fetchone()[0] == 0
    unrelated_after = [
        dict(row) for row in conn.execute(
            "SELECT project_id, type, key, value, source "
            "FROM capability_secrets ORDER BY id"
        ).fetchall()
    ]
    assert unrelated_after == unrelated_before


def test_apply_is_idempotent(conn: Any) -> None:
    migration.apply(conn)
    migration.apply(conn)
    conn.commit()

    migration.invariants(conn)


def test_invariants_detect_retired_rows(conn: Any) -> None:
    with pytest.raises(AssertionError, match="3 retired"):
        migration.invariants(conn)


def test_apply_requires_capability_secrets_table() -> None:
    name = pg_testdb.create_test_database()
    connection = pg_testdb.connect_test_database(name)
    try:
        with pytest.raises(AssertionError, match="table is required"):
            migration.apply(connection)
    finally:
        connection.close()
        pg_testdb.drop_test_database(name)
