"""Generate POSIX shell variable assignments and functions from Python domain modules.

CLI tool that outputs shell code matching the hand-maintained
``approval-vocabulary.sh`` registry, derived from the Python single source of
truth in ``yoke_core.domain.approval``.

Usage::

    python3 -m yoke_core.tools.lifecycle_export approval
"""

from __future__ import annotations

import sys
from typing import FrozenSet, Sequence


def _shell_space_list(items: Sequence[str] | FrozenSet[str]) -> str:
    """Join items with spaces for shell variable assignment."""
    return " ".join(items)


def _case_arms(values: Sequence[str] | FrozenSet[str]) -> str:
    """Build the match pattern for a shell ``case`` arm from *values*."""
    return "|".join(sorted(values))


# ---------------------------------------------------------------------------
# Approval export
# ---------------------------------------------------------------------------


def generate_approval() -> str:
    """Return POSIX shell code for approval constants and functions."""
    from yoke_core.domain.approval import (
        APPROVAL_SCOPE,
        STAGE_AUTHORITY_FIELD,
        STAGE_CACHE_FIELD,
        ApprovalAction,
        HaltState,
    )

    halt_values = tuple(h.value for h in HaltState)
    action_values = tuple(a.value for a in ApprovalAction)

    lines = [
        '#!/usr/bin/env sh',
        '# Auto-generated by lifecycle_export.py -- DO NOT EDIT',
        '# Source of truth: yoke_core.domain.approval',
        '',
        f'APPROVAL_SCOPE="{APPROVAL_SCOPE}"',
        f'APPROVAL_HALT_STATES="{_shell_space_list(halt_values)}"',
        f'APPROVAL_ACTIONS="{_shell_space_list(action_values)}"',
        '',
        f'STAGE_AUTHORITY_FIELD="{STAGE_AUTHORITY_FIELD}"',
        f'STAGE_CACHE_FIELD="{STAGE_CACHE_FIELD}"',
        '',
        '# -- Helper Functions --',
        '',
        'is_halt_state() {',
        '  case "$1" in',
        f'    {_case_arms(halt_values)}) return 0 ;;',
        '    *) return 1 ;;',
        '  esac',
        '}',
        '',
        'is_approval_action() {',
        '  case "$1" in',
        f'    {_case_arms(action_values)}) return 0 ;;',
        '    *) return 1 ;;',
        '  esac',
        '}',
        '',
    ]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------


def main() -> int:
    if len(sys.argv) < 2 or sys.argv[1] != "approval":
        print(
            "Usage: python3 -m yoke_core.tools.lifecycle_export approval",
            file=sys.stderr,
        )
        return 1

    print(generate_approval(), end="")
    return 0


if __name__ == "__main__":
    import os
    # When invoked as a file path, the repo
    # root isn't on sys.path.  Walk up from this file to find it so that
    # `from yoke_core.domain...` imports resolve without PYTHONPATH.
    _this_dir = os.path.dirname(os.path.abspath(__file__))
    _repo_root = os.path.dirname(os.path.dirname(os.path.dirname(_this_dir)))
    if _repo_root not in sys.path:
        sys.path.insert(0, _repo_root)
    sys.exit(main())
