def default_render_output_dir(project: str, *, create: bool = True) -> Path:
    """Return the scratch-backed default output directory for rendered files."""

    return project_scratch_dir.storage_dir(
        "project-renders", "latest", project=project, create=create
    )


def render_project(
    project: str,
    write: bool = False,
    only: str = "all",
    project_root: Optional[Path] = None,
    output_dir: Optional[Path] = None,
    settings: Optional[ProjectRendererSettings] = None,
    pulumi_stack: Optional[str] = None,
) -> None:
    """Main entry: gather values and render requested artifact types.

    ``settings`` injects a preloaded renderer-settings value (e.g. the
    pulumi-stack-config endpoint snapshot); when present, no render step
    touches the database.
    """
    if project_root is None:
        project_root = _resolve_project_root()

    if output_dir is not None:
        proj_dir = Path(output_dir).expanduser()
        if write:
            proj_dir.mkdir(parents=True, exist_ok=True)
    elif write:
        proj_dir = default_render_output_dir(project)
    else:
        proj_dir = default_render_output_dir(project, create=False)

    if pulumi_stack is not None and only != "pulumi":
        raise ValueError("pulumi_stack requires only='pulumi'")
    values = gather_pulumi_values(
        project, project_root, settings, pulumi_stack=pulumi_stack,
    )

    if only in ("all", "DEPLOY.md"):
        render_deploy_md(project, values, project_root, proj_dir, write)
    if only in ("all", "DEPLOY-checklist.md"):
        render_deploy_checklist(project, values, project_root, proj_dir, write)
    if only in ("all", "RECOVERY.md"):
        render_recovery_md(project, values, project_root, proj_dir, write)
    if only in ("all", "workflows"):
        render_workflows(project, values, project_root, proj_dir, write)
    if only in ("all", "scaffold"):
        render_scaffold(project, values, project_root, proj_dir, write)
    if only in ("all", "ops"):
        render_ops(project, values, project_root, proj_dir, write)
    if only in ("all", "pulumi"):
        render_pulumi_artifacts(
            project, values, project_root, proj_dir, write, settings,
            pulumi_stack=pulumi_stack,
        )


