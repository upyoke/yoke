"""Project template renderer for docs, workflows, scaffold, ops, and Pulumi.

Values come from DB-backed cloud-runtime settings homes. ``--write`` saves to an
explicit ``--output-dir`` or a scratch-backed project render directory; without
``--write`` the renderer prints output to stdout.
"""

from __future__ import annotations

import re
import sys
from pathlib import Path
from typing import Dict, List, Optional

from yoke_core.domain import project_scratch_dir

from .project_renderer_pulumi import gather_pulumi_values, render_pulumi_artifacts
from .project_renderer_settings import ProjectRendererSettings
from .project_renderer_values import (  # noqa: F401
    _cap_settings_query,
    _json_field,
    _project_display_name,
    _resolve_project_root,
)


def render_template(content: str, values: Dict[str, str]) -> str:
    """Replace ``{{placeholder}}`` tokens; longest-first to avoid partial matches."""
    for key in sorted(values.keys(), key=len, reverse=True):
        content = content.replace(f"{{{{{key}}}}}", values[key])
    return content


def _auto_header(comment_prefix: str, template_rel: str, project: str, only_flag: str) -> str:
    """Build the auto-generated header for a rendered artifact."""
    return (
        f"{comment_prefix} AUTO-GENERATED by yoke_core.tools.render_project — do not edit\n"
        f"{comment_prefix} Source template: {template_rel}\n"
        f"{comment_prefix} Regenerate: python3 -m yoke_core.tools.render_project"
        f" {project} --write --only {only_flag}\n"
        f"{comment_prefix}"
    )


def _render_doc(
    template_name: str,
    output_name: str,
    values: Dict[str, str],
    project_root: Path,
    proj_dir: Path,
    write: bool,
) -> None:
    """Render one doc template under templates/webapp/ops/ into proj_dir."""
    template = project_root / "templates" / "webapp" / "ops" / template_name
    if not template.is_file():
        return
    rendered = render_template(template.read_text(), values)
    if write:
        (proj_dir / output_name).write_text(rendered + "\n")
        print(f"Rendered: {proj_dir / output_name}", file=sys.stderr)
    else:
        print(rendered)


def render_deploy_md(project, values, project_root, proj_dir, write) -> None:
    """Render DEPLOY.md."""
    _render_doc("DEPLOY.md", "DEPLOY.md", values, project_root, proj_dir, write)


def render_deploy_checklist(project, values, project_root, proj_dir, write) -> None:
    """Render DEPLOY-CHECKLIST.md into DEPLOY-checklist.md."""
    _render_doc("DEPLOY-CHECKLIST.md", "DEPLOY-checklist.md", values, project_root, proj_dir, write)


def render_recovery_md(project, values, project_root, proj_dir, write) -> None:
    """Render RECOVERY.md (disaster-recovery runbook split out of DEPLOY.md)."""
    _render_doc("RECOVERY.md", "RECOVERY.md", values, project_root, proj_dir, write)


def render_workflows(
    project: str,
    values: Dict[str, str],
    project_root: Path,
    proj_dir: Path,
    write: bool,
) -> None:
    """Render workflow YAMLs from templates."""
    ops_dir = project_root / "templates" / "webapp" / "ops"
    project_name = values["project_name"]

    # Workflow name mapping
    name_map = {
        "ephemeral-deploy.yml": f"{project_name}-ephemeral.yml",
        "ephemeral-teardown.yml": f"{project_name}-ephemeral-teardown.yml",
        "deploy.yml": f"{project_name}-deploy.yml",
        "hotfix.yml": f"{project_name}-hotfix.yml",
        "smoke.yml": f"{project_name}-smoke.yml",
    }

    for wf_template in sorted(ops_dir.glob("*.yml")):
        basename = wf_template.name
        out_name = name_map.get(basename, f"{project_name}-{basename}")

        rendered = render_template(wf_template.read_text(), values)
        # Strip template-only comment block
        rendered = re.sub(r'^# Template variables:.*?(?=\n\n|\Z)', '', rendered,
                          flags=re.MULTILINE | re.DOTALL)

        header = _auto_header("#", f"templates/webapp/ops/{basename}",
                              project, "workflows")
        rendered = f"{header}\n{rendered}"

        if write:
            wf_dir = proj_dir / "workflows"
            wf_dir.mkdir(parents=True, exist_ok=True)
            (wf_dir / out_name).write_text(rendered + "\n")
            print(f"Rendered: {wf_dir / out_name}", file=sys.stderr)
        else:
            print(f"--- {out_name} ---")
            print(rendered)


def render_scaffold(
    project: str,
    values: Dict[str, str],
    project_root: Path,
    proj_dir: Path,
    write: bool,
) -> None:
    """Render scaffold files that contain ``{{placeholders}}``.

    Scaffold entries are declared as ``(source_rel, output_rel)`` pairs so the
    source filename can carry a ``.tmpl`` suffix (zero-shell cutover: template
    sources must not be tracked as ``.sh`` files) while the output keeps the
    operator-facing extension (``.sh`` on disk at deploy time).
    """
    scaffold_dir = project_root / "templates" / "webapp" / "scaffold"
    scaffold_files = [
        ("docker-compose.yml", "docker-compose.yml"),
        ("app/Dockerfile", "app/Dockerfile"),
        ("app/entrypoint.sh.tmpl", "app/entrypoint.sh"),
        ("app/web/Dockerfile", "app/web/Dockerfile"),
        ("app/web/next.config.ts", "app/web/next.config.ts"),
    ]

    for src_rel, out_rel in scaffold_files:
        sf_template = scaffold_dir / src_rel
        if not sf_template.is_file():
            continue

        rendered = render_template(sf_template.read_text(), values)
        out_basename = Path(out_rel).name
        out_ext = Path(out_rel).suffix.lstrip(".")

        header = _auto_header("#", f"templates/webapp/scaffold/{src_rel}",
                              project, "scaffold")

        if out_basename == "Dockerfile":
            rendered = f"{header}\n{rendered}"
        elif out_ext in ("yml", "yaml"):
            rendered = f"{header}\n{rendered}"
        elif out_ext == "sh":
            # Insert header after shebang
            lines = rendered.split("\n", 1)
            if len(lines) > 1:
                rendered = f"{lines[0]}\n{header}\n{lines[1]}"
            else:
                rendered = f"{lines[0]}\n{header}"
        elif out_ext == "ts":
            ts_header = _auto_header("//", f"templates/webapp/scaffold/{src_rel}",
                                     project, "scaffold")
            rendered = f"{ts_header}\n{rendered}"

        if write:
            out_path = proj_dir / "scaffold" / out_rel
            out_path.parent.mkdir(parents=True, exist_ok=True)
            out_path.write_text(rendered + "\n")
            print(f"Rendered: {out_path}", file=sys.stderr)
        else:
            print(f"--- scaffold/{out_rel} ---")
            print(rendered)


def render_ops(
    project: str,
    values: Dict[str, str],
    project_root: Path,
    proj_dir: Path,
    write: bool,
) -> None:
    """Render ops programs (.sh.tmpl, .py, .conf, .js).

    Shell templates are stored as ``*.sh.tmpl`` (zero-shell cutover:
    no ``.sh`` file is tracked as a template source). The rendered output
    filename strips the ``.tmpl`` suffix and is written as ``*.sh`` at the
    operator's machine for scp-to-VPS deployment.
    """
    ops_dir = project_root / "templates" / "webapp" / "ops"

    # Explicit program templates render even without project placeholders;
    # .conf / .js retain the established placeholder-only heuristic.
    patterns = (
        ("*.sh.tmpl", True),
        ("*.py", True),
        ("*.conf", False),
        ("*.js", False),
    )

    for pattern, is_explicit_template in patterns:
        for ops_template in sorted(ops_dir.glob(pattern)):
            template_name = ops_template.name

            content = ops_template.read_text()
            # For .sh.tmpl sources we always render, even when the template
            # carries no ``{{placeholder}}`` — the explicit ``.sh.tmpl``
            # extension is the operator's signal that this file is owned by
            # the renderer. For .conf / .js we keep the established
            # heuristic of only rendering files that actually use
            # placeholders, since those glob patterns pick up unowned files.
            if not is_explicit_template and "{{" not in content:
                continue

            rendered = render_template(content, values)
            # Strip template-only comment block
            rendered = re.sub(r'^# Template variables:.*?(?=\n\n|\Z)', '', rendered,
                              flags=re.MULTILINE | re.DOTALL)

            if template_name.endswith(".sh.tmpl"):
                # e.g. ephemeral-cleanup.sh.tmpl -> ephemeral-cleanup.sh
                out_basename = template_name[:-len(".tmpl")]
                out_ext = "sh"
            else:
                out_basename = template_name
                out_ext = ops_template.suffix.lstrip(".")

            comment_prefix = "//" if out_ext == "js" else "#"

            header = _auto_header(comment_prefix,
                                  f"templates/webapp/ops/{template_name}",
                                  project, "ops")

            if out_ext in ("sh", "py"):
                # Insert header after shebang
                lines = rendered.split("\n", 1)
                if len(lines) > 1:
                    rendered = f"{lines[0]}\n{header}\n{lines[1]}"
                else:
                    rendered = f"{lines[0]}\n{header}"
            else:
                rendered = f"{header}\n{rendered}"

            if write:
                ops_out = proj_dir / "ops"
                ops_out.mkdir(parents=True, exist_ok=True)
                out_path = ops_out / out_basename
                out_path.write_text(rendered + "\n")
                out_path.chmod(0o755)
                print(f"Rendered: {out_path}", file=sys.stderr)
            else:
                print(f"--- ops/{out_basename} ---")
                print(rendered)


