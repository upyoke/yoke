# Deterministic activation commit SHA for tests that don't care which
# concrete head the door lock pins.
SNAP = "snap-default"


def _apply_path_claim_schema() -> None:
    """Build the minimal path-claim schema on the repointed test DSN.

    Runs as the ``init_test_db`` ``apply_schema`` strategy: ``db_backend``
    resolves the per-test disposable database the context just repointed
    ``YOKE_PG_DSN`` at, so the substrate lands in the throwaway database,
    not the shared ambient one.

    The schema is deliberately fuller than the path tables alone. On Postgres
    a read against a missing relation aborts the surrounding transaction, and
    the registration on-ramp tolerates absent ``projects`` /
    ``item_dependencies`` / ``event_registry`` rows by swallowing that read.
    Post-abort, that swallow would poison every later statement in the
    caller's transaction (or, for the retired-name guard, trigger a recovery
    rollback that discards the caller's pending path-target / claim rows).
    Materializing those tables empty keeps every tolerated lookup on the
    success path so the caller's transaction survives — the SQLite double the
    fixture replaced had no aborted-transaction state, so it tolerated the
    missing relations silently. ``events_schema.cmd_init`` supplies
    events + severity_config + event_registry against the same repointed DSN.
    """
    c = db_backend.connect()
    try:
        create_core_tables(c)
        c.commit()
    finally:
        c.close()

    events_schema.cmd_init()
    c = db_backend.connect()
    try:
        apply_harness_session_columns(c)
        create_path_registry_tables(c)
        create_actor_path_claim_tables(c)
        c.execute(
            "INSERT INTO projects "
            "(id, slug, name, default_branch, github_repo, "
            "public_item_prefix, created_at) "
            "VALUES "
            "(1, 'yoke', 'Yoke', 'main', NULL, 'YOK', "
            "'2026-05-01T00:00:00Z'), "
            "(2, 'buzz', 'Buzz', 'main', NULL, 'BUZ', "
            "'2026-05-01T00:00:00Z') "
            "ON CONFLICT (id) DO NOTHING"
        )
        c.execute(
            "CREATE TABLE IF NOT EXISTS item_dependencies ("
            "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, "
            "dependent_item TEXT NOT NULL, "
            "blocking_item TEXT NOT NULL, "
            "gate_point TEXT NOT NULL DEFAULT 'activation', "
            "satisfaction TEXT NOT NULL DEFAULT 'status:done', "
            "source TEXT NOT NULL DEFAULT 'test', session_id INTEGER, "
            "rationale TEXT NOT NULL DEFAULT '', "
            "evidence_json TEXT NOT NULL DEFAULT '{}', "
            "created_at TEXT NOT NULL DEFAULT '1970-01-01T00:00:00Z')"
        )
        seed_canonical_actors(c)
        c.commit()
    finally:
        c.close()


@pytest.fixture
def conn(tmp_path):
    with init_test_db(tmp_path, apply_schema=_apply_path_claim_schema) as db_path:
        c = connect_test_db(db_path)
        try:
            yield c
        finally:
            c.close()


def seed_target(conn: Any, *, path_string: str) -> int:
    """Insert a `path_targets` row and return its id."""
    cur = conn.execute(
        "INSERT INTO path_targets "
        "(project_id, kind, path_string, generation, created_at) "
        "VALUES (1, 'directory', %s, 1, '2026-05-01T00:00:00Z') "
        "RETURNING id",
        (path_string,),
    )
    return int(cur.fetchone()[0])


def local_human(conn: Any) -> int:
    """Resolve the seeded local human actor id, falling back to a fresh row."""
    row = conn.execute(
        "SELECT actor_id FROM actor_labels "
        "WHERE label='ben' AND surface='github_label'"
    ).fetchone()
    return int(row["actor_id"]) if row else seed_human_actor(conn)


def seed_item(conn: Any, *, item_id: int, status: str = "idea") -> int:
    """Insert a minimal ``items`` row and return its id."""
    conn.execute(
        "INSERT INTO items (id, title, type, status, priority, "
        "created_at, updated_at, project_id, project_sequence) "
        "VALUES (%s, 'item', 'issue', %s, 'medium', "
        "'2026-05-01T00:00:00Z', '2026-05-01T00:00:00Z', 1, %s)",
        (item_id, status, item_id),
    )
    conn.commit()
    return item_id


def seed_claim(
    conn: Any,
    *,
    item_id: int,
    target_id: int,
    state: str,
    blocked_reason: Optional[str] = None,
) -> int:
    """Insert a ``path_claims`` row plus target binding and return its id."""
    row = conn.execute(
        "INSERT INTO path_claims "
        "(state, mode, actor_id, item_id, integration_target, "
        "registered_at, blocked_reason) "
        "VALUES (%s, 'exclusive', %s, %s, 'main', "
        "'2026-05-01T00:00:00Z', %s) RETURNING id",
        (state, local_human(conn), item_id, blocked_reason),
    ).fetchone()
    claim_id = int(row[0])
    if state == "active":
        conn.execute(
            "UPDATE path_claims SET "
            "activated_at = '2026-05-01T01:00:00Z', base_commit_sha = %s "
            "WHERE id = %s",
            (SNAP, claim_id),
        )
    conn.execute(
        "INSERT INTO path_claim_targets (claim_id, target_id, declared_at) "
        "VALUES (%s, %s, '2026-05-01T00:00:00Z')",
        (claim_id, target_id),
    )
    conn.commit()
    return claim_id


