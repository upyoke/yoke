"""Shared fixtures and seed helpers for the ``test_path_claims_*`` files.

Splits out of ``test_path_claims.py`` after the parent grew past the
350-line gate. Three test files share this fixture surface:

* ``test_path_claims.py`` — register / fixture coverage.
* ``test_path_claims_lifecycle.py`` — activate / release / cancel.
* ``test_path_claims_overlap.py`` — overlap classifier coverage.

The ``conn`` fixture builds the schema substrate the path-claim domain
needs (core + path-registry + actor / path-claim tables) and seeds
canonical actors. Tests that need a deterministic activation SHA use
the ``SNAP`` constant — a string SHA recorded on
``path_claims.base_commit_sha`` at activate time.

The ``conn`` fixture uses a disposable per-test Postgres DB because
these tests exercise real Yoke domain tables, event emission, and
Postgres-native registration/planning SQL. Parser-only or external
validation tests may still own explicit SQLite doubles.
"""

