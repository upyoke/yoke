def count_refresh_events(conn: Any, *, claim_id: int) -> int:
    """Count ``PathClaimBlockedReasonRefreshed`` events for a claim id."""
    rows = conn.execute(
        "SELECT envelope FROM events "
        "WHERE event_name = 'PathClaimBlockedReasonRefreshed'"
    ).fetchall()
    return sum(
        1 for row in rows
        if json.loads(row["envelope"])["context"]["claim_id"] == claim_id
    )


HOLDER_SESSION_ID = "sess-test-holder"
"""Canonical ``harness_sessions.session_id`` for the test holder session.

Used by every ``path-claims`` dispatch test that exercises a mutating
subcommand so the new ambient ownership guard sees a matching active
``work_claims`` row.
"""


def seed_test_holder_session(
    conn: Any, session_id: str = HOLDER_SESSION_ID
) -> None:
    """Insert the canonical test holder ``harness_sessions`` row idempotently."""
    actor_id = local_human(conn)
    if _column_exists(conn, "harness_sessions", "actor_id"):
        conn.execute(
            "INSERT INTO harness_sessions (session_id, executor, "
            "provider, model, project_id, execution_lane, capabilities, workspace, mode, "
            "offered_at, last_heartbeat, actor_id) "
            "VALUES (%s, 'test', 'test', 'test', 1, 'primary', '[]', '/tmp', "
            "'active', '2026-05-01T00:00:00Z', '2026-05-01T00:00:00Z', %s) "
            "ON CONFLICT(session_id) DO UPDATE SET actor_id = EXCLUDED.actor_id",
            (session_id, actor_id),
        )
        return
    conn.execute(
        "INSERT INTO harness_sessions (session_id, executor, "
        "provider, model, project_id, execution_lane, capabilities, workspace, mode, "
        "offered_at, last_heartbeat) "
        "VALUES (%s, 'test', 'test', 'test', 1, 'primary', '[]', '/tmp', "
        "'active', '2026-05-01T00:00:00Z', '2026-05-01T00:00:00Z') "
        "ON CONFLICT(session_id) DO NOTHING",
        (session_id,),
    )


def seed_work_claim_for(
    conn: Any, *, item_id: int,
    session_id: str = HOLDER_SESSION_ID,
) -> None:
    """Insert a live ``work_claims`` row owned by the test holder."""
    conn.execute(
        "INSERT INTO work_claims (session_id, target_kind, item_id, "
        "claimed_at, last_heartbeat) "
        "VALUES (%s, 'item', %s, '2026-05-01T00:00:00Z', "
        "'2026-05-01T00:00:00Z')",
        (session_id, int(item_id)),
    )


def seed_test_holder_for(conn: Any, *, item_id: int) -> None:
    """Seed both the canonical holder ``harness_sessions`` row and its claim."""
    seed_test_holder_session(conn)
    seed_work_claim_for(conn, item_id=item_id)


